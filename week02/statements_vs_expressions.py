# Expressions vs Statements
# Expressions evaluate to a value
# Statement performs an action

3   # expression
3.3 # expression
x = 4 # statement
x += 5 # statement
4 > 3 # expression
"Hello"  # expression
def greet():   # function definition, statement
    return "Hello "

greet  # expression
greet() # expression
print  # expression
print("Hello") # expression

import math  # statement
